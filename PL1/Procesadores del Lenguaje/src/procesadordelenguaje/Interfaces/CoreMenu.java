    private void MEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MEActionPerformed
        try{
            int length = Integer.parseInt(VariablesLengthJTextField.getText());
            int number = Integer.parseInt(VariablesNumberJTextField.getText());
            if(length<=0 || number<= 0){
                Consola.setText("ERROR: Los valores introducidos en los recuadros han de ser superiores a 0");
            }else{
                new MatrixAFDInput(number,length,alfabeto,ER,this).setVisible(true);
                this.setVisible(false);
            }
        }catch(Exception e){
            Consola.setText("ERROR: Los valores introducidos en los recuadros ha de ser de tipo numerico");
        }
        

    }//GEN-LAST:event_MEActionPerformed

